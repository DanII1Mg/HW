# Реализовать 4 и 5 теоремы

def Theorem_4(k):
    """
    Реализация теоремы Лагранжа в строгой формулировке:
    R = 1 + корень степени (n - i) из (|a_i| / a_n), где a_i - первый отрицательный коэффициент
    """
    n = len(k) - 1
    a_n = k[0]
    if a_n <= 0:
        raise ValueError("Коэффициент при старшей степени должен быть положительным")

    # Поиск первого отрицательного коэффициента
    for i in range(1, n + 1):
        if k[i] < 0:
            power = n - i
            c = abs(k[i])
            R = 1 + (c / a_n) ** (1 / power)
            return R

    return

print(Theorem_4([3, -5, 10, -1]))